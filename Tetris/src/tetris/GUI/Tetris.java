    private void bntCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCriarActionPerformed
        // TODO add your handling code here:
        String strLinhas = spnLinhas.getValue().toString();
        int linhas = Integer.parseInt(strLinhas); 
        
        String strColunas = spnColunas.getValue().toString();
        int colunas = Integer.parseInt(strColunas); 
        
        game = new Board(linhas,colunas);
        String board = game.toString();
        txtBoard.setText(board);
    }//GEN-LAST:event_bntCriarActionPerformed

