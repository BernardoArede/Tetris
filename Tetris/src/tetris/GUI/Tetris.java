    private void btnDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownActionPerformed
        // TODO add your handling code here:
        game.moveDown();
        String baixo = game.toString();
        txtBoard.setText(baixo);

        if (game.canMovePiece(1,0)) {
            game.moveDown();
        } else {
            // A peça não pode mais se mover para baixo
            game.freezePiece(); // Congela a peça atual

            // Cria uma nova peça aleatória
            game.generateRandomPiece();
        }

        String boardString = game.toString();
        txtBoard.setText(boardString);
    }//GEN-LAST:event_btnDownActionPerformed

